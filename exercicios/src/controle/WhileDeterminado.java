package controle;

public class WhileDeterminado {

	public static void main(String[] args) {


		
		int num = 0;
		
		/*
		 while retorna true ou false, enquanto for true, 
		 ele executa o bloco de comandos, caso seja false, 
		 ele não o executa mais
		*/
		while(num<=10) {
			System.out.printf("variável = %d\n", num);
			num++;
		}
		

		
	}

}
